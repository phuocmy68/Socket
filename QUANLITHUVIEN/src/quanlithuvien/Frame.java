    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String keyword = tf5.getText();

        // Kiểm tra xem có từ khóa tìm kiếm không
        if (!keyword.isEmpty()) {
            // Trong phương thức jButton4ActionPerformed
            DAO1 dao = new DAO1(); // Sử dụng đúng đối tượng DAO1
            ArrayList<Students> searchResult = dao.search(keyword);

            // Hiển thị kết quả tìm kiếm trên bảng
            displaySearchResult(searchResult);
        } else {
            // Nếu từ khóa trống, hiển thị thông báo
            JOptionPane.showMessageDialog(this, "Please enter the keywords you want to search for!");
        }
    }

