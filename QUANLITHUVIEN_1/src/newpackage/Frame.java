    private void loadDataIntoTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);

        DAO dao = new DAO();
        ArrayList<Students> studentList = dao.getList();

        for (Students st : studentList) {
            model.addRow(new Object[]{st.getMSV(), st.getHoten(), st.getNgaysinh(), st.getLop()});
        }
    }
