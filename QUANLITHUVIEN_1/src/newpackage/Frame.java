    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) {
            String msvtodelete = model.getValueAt(selectedRow, 0).toString();

            DAO dao = new DAO();
            Students sttodelete = new Students();
            sttodelete.setMSV(msvtodelete);

            dao.delete(sttodelete);
            model.removeRow(selectedRow);

            tf1.setText("");
            tf2.setText("");
            tf3.setText("");
            tf4.setText("");

            JOptionPane.showMessageDialog(this, "Delete successful!");
            loadDataIntoTable();
        } else if (table.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Table is empty!");
        } else {
            JOptionPane.showMessageDialog(this, "Select a row for deletion!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

