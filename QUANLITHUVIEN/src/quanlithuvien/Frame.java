    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String msv = tf1.getText();
        String name = tf2.getText();
        String dob = tf3.getText();
        String cl = tf4.getText();

        DefaultTableModel model = (DefaultTableModel) table.getModel();

        int selectedRow = table.getSelectedRow();

        // Check if a row is selected
        if (selectedRow != -1) {
            model.setValueAt(msv, selectedRow, 0);
            model.setValueAt(name, selectedRow, 1);
            model.setValueAt(dob, selectedRow, 2);
            model.setValueAt(cl, selectedRow, 3);

            newpackage.Students updatestudents = new newpackage.Students(msv, name, dob, cl);
            DAO dao = new DAO();
            dao.update(updatestudents);

            loadDataIntoTable();
            tf1.setText("");
            tf2.setText("");
            tf3.setText("");
            tf4.setText("");
        } else {
            // Optionally show a message or take some action if no row is selected
            JOptionPane.showMessageDialog(this, "Please select a row to update.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) {
            String msvtodelete = model.getValueAt(selectedRow, 0).toString();

            DAO dao = new DAO();
            newpackage.Students sttodelete = new newpackage.Students();
            sttodelete.setMSV(msvtodelete);

            dao.delete(sttodelete);
            model.removeRow(selectedRow);

            tf1.setText("");
            tf2.setText("");
            tf3.setText("");
            tf4.setText("");

            JOptionPane.showMessageDialog(this, "Delete successful!");
            loadDataIntoTable();
        } else if (table.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Table is empty!");
        } else {
            JOptionPane.showMessageDialog(this, "Select a row for deletion!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

